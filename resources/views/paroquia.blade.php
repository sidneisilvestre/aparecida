Os liguistas participavam das obras de construção da nova igreja da Vila Barcelona. Havia a obrigatoriedade de uso do distintivo da Liga na lapela dos paletós e dos cordões nas missas e terços. Os membros da diretoria usavam cordões vermelhos e os liguistas sem cargos usavam cordões verdes.
Havia um caráter comunitário entre os membros da Liga, que promoviam visitas às casas, auxiliavam nas quermesses, faziam intercâmbio com as demais Ligas da região. Em São Caetano, em 1956, existiam as seguintes ligas: da Sagrada Família, Nossa Senhora as Candelária, Nossa Senhora das Graças, Sagrado Coração de Jesus (Vila São José), Bairro Fundação e Vila Prosperidade.
</p>
<h2>A imagem de Nossa Senhora Aparecida</h2>
<p class="text-justify">
Em outubro de 1956, na maior empolgação religiosa, a Vila Barcelona festejou muito a chegada e a entronização da imagem de Nossa Senhora Aparecida, oferecida pelo padre Donizzetti, de Tambaú, de muita popularidade à época.
</p><p class="text-justify">
A imagem é semelhante a imagem original encontrada no Rio Paraíba e guardada na Basílica Nacional de Aparecida, porém feita de madeira.
</p>
<h2>A nova igreja</h2>
<p class="text-justify">
No começo da década de 60, o padre Canísio começou uma nova campanha pela construção de um templo maior, pois a população do bairro havia crescido muito desde 1955 e a capela já não comportava mais a comunidade. Desta vez porém o sonho do povo católico demorou a se concretizar.
</p><p class="text-justify">
Em 1966 o padre Canísio afastou-se e assumiu provisoriamente a paróquia o padre Jorge Nogueira, que muito lutou para iniciar a construção, mas foi somente com a nomeação do jovem e dinâmico padre Olavo Paes de Barros Filho como pároco, em fevereiro de 1968, que o sonho do novo templo começou a se realizar.O Pe. Olavo, assim que assumiu, começou a dinamizar e motivar os movimentos e pastorais existentes na paróquia, como o Apostolado da Oração, a Legião de Maria e os Irmãos do Santíssimo, dando também condições para uma catequese consciente e responsável.
</p><p class="text-justify">
Em maio de 1968 fundou a Associação das Senhoras Católicas, que além de participar das celebrações litúrgicas, trabalhava para angariar fundos e poder manter as necessidades da paróquia. Através reuniões, debates e planos conseguiu formar a Comissão Pró-Construção da nova igreja, comissão liderada por ele, padre Olavo, e contando com os seguintes colaboradores: vereador Sebastião Laureano dos Santos, Cyro Rocha, José Tenório da Silva, João Gallo e Mª Aparecida Valdarmini Lopes, então presidente da Associação das Senhoras Católicas.
</p><p class="text-justify">
Já no fim de 1968 a construção da nova igreja estava em franco progresso e com a visita do Cardeal Dom Agnelo Rossi, Arcebispo de São Paulo, o movimento pró-construção recebeu um grande incentivo.
</p><p class="text-justify">
Em abril de 1972 foi possível iniciar a utilização do novo templo, mesmo com a obra ainda inacabada, e foi então demolida a velha capela.</p>
<h2>A imagem do Cristo no altar</h2>

<p class="text-justify">Tire as sandálias, o lugar que estás é santo

Em 1972, o então pároco Padre Olavo Paes de Barros, encomendou do artista plástico Juliano Gonzáles Garcia uma imagem em madeira mogno, de Jesus na cruz. Na ocasião, ele queria uma imagem que representasse Jesus já glorificado, após a Ressurreição, e que transmitisse serenidade e paz.
 
Padre Olavo e Dª Maria Aparecida Valdarmini, secretária da igreja, se revezavam na tarefa diária de visitar o ateliê do artista, na cidade vizinha de Rio Grande da Serra, a fim de acompanhar a obra.

O artesão usou três troncos de mogno que ele mesmo preparou, recortando em pranchas e unindo-as para realizar a escultura.
Com tudo pronto, ele começou a esculpir o Cristo pelos pés, tomando o máximo de cuidado para não perder material, e meses depois, ja com o trabalho bem avançado e, faltando apenas o rosto de Nosso Senhor, o artesão pediu que eles não fossem mais até lá, e que apenas aguardassem. 

Algumas semanas depois, quando padre Olavo ja estava angustiado por falta de notícias, foi chamado pelo artesão.
Quando chegou, o rosto de Jesus estava coberto com um pano. Juliano contou, que diferente dos outros dias em que não tivera inspiração para finalizar a imagem, acordara às 5 horas da manhã com a face de Cristo na mente, e começou a trabalhar sem cessar até concluir, por volta de meio-dia.

Quando finalmente retiraram o pano que cobria a face de Cristo, padre Olavo emocionou-se, pois o artista conseguiu realizar o que ele pedira: o rosto de Jesus nos transmite serenidade, paz e alegria para aqueles que o contemplam.

A imagem, com 3,5 metros de altura e pesando aproximadamente uma tonelada, foi entregue em 1973 e colocada no centro do altar.

</p>
<h2>A procissão de Corpus Christi e o Campanário</h2>
<p class="text-justify">Nesse mesmo ano de 1973, o padre Olavo incentivou a confecção de tapetes de serragem colorida e outros materiais nas principais ruas do bairro para a Procissão de Corpus Christi, costume que se manteve até o inicio dos anos 80.
Em 1976 foi entregue o Campanário, obra no mesmo estilo moderno da igreja e que representa a pesca milagrosa da imagem de Nossa Senhora Aparecida no Rio Paraiba.</p>
