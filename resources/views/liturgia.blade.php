		<h1 id="conteudo">Pastoral da Liturgia</h1>
<p class="text-justify">Liturgia é a ação do Povo de Deus, reunido em Jesus Cristo, na comunhão do Espírito Santo. É sempre uma celebração de Mistério Pascal, isto é, passagem da morte para vida, através de sinais, gestos e palavras. A liturgia é ação de Cristo na Igreja.
</p><p class="text-justify">
O ponto culminante de uma comunidade eclesial é a celebração comunitária, onde todos expressam sua fé comum; ouvem o mesmo Senhor, Salvador e Libertador; agradecem os favores de Deus; cantam as mesmas canções. Aí todos louvam a Deus e os laços do amor fortalecidos. Cresce a fraternidade e o Povo de Deus se reanima, sobretudo nos Sacramentos, para continuar a luta pela construção do Reino.
</p><p class="text-justify">
Nenhuma atividade pastoral pode realizar-se sem referencia à liturgia. Qualquer celebração tem sentido evangelizador e catequético. Toda ação pastoral terá como ponto de referencia a liturgia, na qual se celebra a memória e se proclama a atualidade do projeto de Jesus Cristo. (Conf. Doc. 38 —CNBB).
</p><p class="text-justify">
A celebração litúrgica, como a obra de Cristo Sacerdote, e da Igreja que é seu Corpo, é uma ação sagrada por excelência. Sua eficácia não é igualada por nenhuma outra ação da Igreja. (Conf. SC 7)
</p>
<h2>Quando falamos de liturgia, temos presente:</h2><br>
• A Missa ou Celebração Eucarística;<br>
• Celebração dos Sacramentos (batismo, crisma, eucaristia, penitencia, unção dos enfermos, ordenação, matrimonio);<br>
• A Celebração dos Sacramentais (bênçãos, encomendação dos mortos...);<br>
• A Celebração da Palavra ou Culto;<br>
• A Liturgia das Horas;<br>
• O Ano Litúrgico.<br>

<h2>A necessária organização litúrgica</h2>
<p class="text-justify">Todos sabemos que nenhuma atividade na comunidade funciona sem um mínimo de organização. A liturgia não foge desta necessidade. Para que a dimensão celebrativa funcione bem. Para que haja participação de todos, se faz necessário que alguém, uma equipe pense, planeje, prepare com carinho e dedicação.
</p><p class="text-justify">
As celebrações das comunidades, das paróquias e das Dioceses precisam de um grupo de pessoas, de uma equipe ou de várias equipes que prestem esse serviço comunitário. O primeiro critério que esta equipe ou pessoas devem ter presente, é o "Querer Celebrar Bem". Celebrar de tal modo que favoreça a participação de todos os presentes. </p>
		
