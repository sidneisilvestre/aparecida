		<h1 id="conteudo">Irmandade do Santíssimo</h1>
<p class="text-justify">A Irmandade do Santíssimo é uma associação religiosa, composta por católicos apostólicos romanos de ambos os sexos, maiores de 16 anos. Tem o pároco, Pe. Luiz Francisco, como o Provedor de Honra. 
</p><p class="text-justify">
O fim principal da Irmandade é manter o culto de adoração ao Santíssimo Sacramento. E para alcançar tal objetivo, usamos de meios devidamente autorizados pela Autoridade Eclesiástica, promovendo ou auxiliando nas atividades e comemorações:
</p><p class="text-justify">
<h2>Sexta-feira antes do Domingo de Ramos:</h2>
• Cortar ramos (oliva e palmeiras).
<h2>Sábado:</h2>
• Confeccionar os ramalhetes, a partir das 7h00.
<h2>Domingo de Ramos:</h2>
• Distribuição dos ramalhetes a partir das 8h30.
• Participação na Benção dos ramos e na procissão, todos devidamente uniformizados.
<h2>Terça-feira Santa:</h2>
• Participação na procissão, todos devidamente uniformizados.
<h2>Quinta-feira Santa:</h2>
• Participação na cerimônia do Lava-Pés, e na procissão dentro da igreja.
<h2>Sexta-feira Santa:</h2>
• Montar guarda ao Santíssimo enquanto estiver exposto.
• Participação do grupo na Hora Santa.
• Participação de irmãos na Via-Sacra.
• Distribuição de velas que serão utilizadas na procissão.
• Participação na procissão, todos devidamente uniformizados.
<h2>Sábado Santo:</h2>
• Vigília Pascal, com participação de todos devidamente uniformizados.
<h2>Domingo de Páscoa:</h2>
• Participação na procissão da Ressurreição.
<h2>Corpus Christi:</h2>
• Conforme a Liturgia exige.

<h2>Compromisso do Grupo:</h2>
• Todos as vezes que o Santíssimo estiver exposto, montar-se-á a Guarda de Honra.
• Todos os dias 12 de cada mês, reza do Terço na igreja às 18h00. (exceto aos sábados e domingos).
• Todas as primeiras quintas-feiras do mês, às 18h00 na igreja fazer Hora Santa.
• Promover e participar durante a quaresma da Campanha da Fraternidade.
• No mês de dezembro promover e participar da novena de Natal em Família.
• Ao longo do ano rezar o terço em casas de famílias.
• Participação em todas as procissões.
• Todo último domingo do mês, Missa Solene às 7h00 com participação em toda a Litúrgia, e reunião ordinária após a Missa.
• Quando a pedido do padre, participação nas liturgias.
<h2>Tanto a ala masculina como a feminina é composta por:</h2>
• Provedor/a - Vice Provedor/a - Secretário/a - Tesoureiro/a.
<p class="text-justify">
Atualmente fazem parte do grupo quarenta e cinco (45) pessoas, sendo vinte e três (23) homens e vinte e duas (22) mulheres.
