<h2>ORIENTAÇÕES PARA OS NOIVOS </h2>
<h3>1. RESERVA DE DATA</h3>
a) Ao escolher a nossa igreja para a celebração do matrimônio, a primeira providência é 
marcar com antecedência de 3 meses a data e o horário do casamento. Em nossa 
igreja são realizados às sextas-feiras às 20 horas e aos sábados das 18 às 20:30 horas. 
<br/>
b) A reserva somente é feita mediante a um sinal. Se houver desistência, não há 
devolução dos valores pagos. Caso seja necessário e havendo disponibilidade, é 
permitida a mudança de data e horário. <br/>
<h3>2. PROCESSO MATRIMONIAL </h3>
Os noivos deverão, 3 meses antes da data do casamento, solicitar a Transferência de 
Paróquia, processo que deve ser feito na Paróquia mais próxima à residência da noiva 
ou do noivo. <br/>
<h4>Os documentos necessários a serem apresentados por ambos para o processo normal são: </h4>
• Documento de Identidade, <br/>
• Certidão de batismo recente ou atualizada para fins matrimoniais (com data 
inferior a 6 meses do casamento)<br/>
• Certificado do curso de preparação para o matrimônio (curso de noivos). <br/>
Os noivos deverão retornar 1 mês antes da data do casamento trazendo os seguintes 
documentos: Relação de padrinhos (formulário fornecido na secretaria da igreja) e 
Certidão de habilitação do cartório, caso seja com efeito civil. 
<h3>3. CASAMENTO CIVIL </h3>
Civil realizado antes do religioso: entregar cópia da certidão de casamento civil antes 
da cerimônia religiosa. <br/>
Religioso com efeito civil: segundo a lei vigente, oficiado pelo sacerdote celebrante, 
juntamente com o casamento religioso. O casal deverá procurar o cartório para 
solicitar a certidão de habilitação, onde será orientado, levando desta igreja o 
requerimento. 
 <br/>
<h3>4. PADRINHOS </h3>
Duas testemunhas, maiores de idade, assinarão a ata da celebração do casamento 
religioso. Caso o casamento seja com efeito civil, mais duas testemunhas assinam. O 
número máximo de padrinhos permitido no altar é de quatro casais para a noiva e 
quatro casais para o noivo. <br/>
<h3>5. DECORAÇÃO </h3>
a) Realizada por uma das floriculturas indicadas. Os noivos, de comum acordo devem 
providenciar e custear as despesas. <br/>
b) Os arranjos e enfeites no Altar deverão ser bem 
posicionados de forma para não atrapalhar a presença dos padrinhos. <br/>
c) Não serão permitidos enfeites nos bancos com espadas, velas ou estátuas. <br/>
d) Os enfeites nos bancos deverão ter seus respectivos suportes para não molhar ou 
causar danos aos bancos. <br/>
e) O uso de tapetes é de responsabilidade da floricultura. <br/>
f) Não são de responsabilidade da Igreja os arranjos, enfeites e as músicas. <br/>
<h3>6. MÚSICA </h3>
a) A música deverá criar um ambiente de meditação e de festiva religiosidade, 
evocando a Deus e ao sagrado. <br/>
b) Os temas musicais deverão ser passados por consulta para aprovação. Não é 
permitido temas de conotação profana que sugiram filmes ou novelas. <br/>
c) Realizada pelo coral indicado. São permitidas no máximo três entradas (3 músicas): 
cortejo, damas e noiva. <br/>
<h3>7. IMPORTANTE </h3>
a) Menores de 18 anos: caso o noivo ou a noiva ou ambos não tenham completado 18 
anos de idade devem obter consentimento dos pais ou responsáveis, comparecendo 
acompanhado de um deles para a respectiva autorização na igreja onde o casamento 
se realizará. <br/>
b) Celebrante de outra comunidade: é facultativo convidar um sacerdote amigo de 
outra comunidade para presidir o casamento, pede-se confirmar com antecedência o 
nome completo do sacerdote. <br/>
c) Pontualidade: o noivo, os pais e padrinhos devem chegar com 20 minutos de 
antecedência do horário para receberem as devidas orientações. O atraso implicará na 
celebração somente após a realização de todos os casamentos do dia. <br/>
d) Entrada da noiva: a noiva deve estar pontualmente no seu horário e se preparar 
para entrar na igreja. <br/>
e) Floristas: para a segurança dos noivos, padrinhos e convidados, não é permitido 
jogar pétalas de rosas ao longo do corredor ou na frente da igreja. <br/>
f) Celebração: a celebração do Sacramento do Matrimônio é predominantemente 
religiosa. Certa conotação social não poderá ser essencial. Enfeites, arranjos, 
reportagens e temas musicais estarão eventualmente como complementação. <br/>
g) Capacidade: a igreja abriga cerca de 500 pessoas sentadas.
 
